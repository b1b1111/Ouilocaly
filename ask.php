<h2>LES QUESTIONS SUR OUILOCALY</h2>

<h3>A quel type de commerçant s’adresse Ouilocaly ?</h3>

<p>
    Cela s’adresse à tout le monde, commerces indépendants en tête. Ce qui compte est l’offre des
    commerçants et l’endroit où ils situent. La taille ou le budget ne sont pas les variables.
    Si ce que propose le commerçant correspond à ce que vous recherchez, le commerçant sera visible
    dans les résultats qui vous seront transmis. Ensuite le classement correspond à la proximité. Plus le
    commerce est proche de vous et plus il remontera dans les résultats. S’il est le plus proche, il
    remontera en premier, c’est aussi simple que cela. La notion de proximité prend ainsi tout son sens.
    Au travers des ventes proposées, au-delà de donner à voir ce qu’il propose, le commerçant peut ainsi
    se positionner dans les résultats sur les produits que vous recherchez.
</p>

<h3>Où est présent Ouilocaly ?</h3>

<p>
    Aujourd’hui, Ouilocaly peut potentiellement être présent partout en France !
    Pour commencer et étant une application basée à Nantes, nous nous développons plus
    spécifiquement en Loire Atlantique (Nantes et sa région ainsi que Saint-Nazaire et sa région).
    Mais très rapidement, nous allons chercher à étendre cette zone pour être présent partout en
    France. Nous restons d’ailleurs disponibles pour avancer avec les villes qui auraient la volonté de
    mettre en valeur leurs commerces de centre-ville sur un support dédié, de manière simple,
    financièrement neutre et activable très rapidement. N’hésitez pas à nous contacter pour plus
    d’informations.<br />
    Notre objectif est qu’où que vous soyez en France, vous puissiez trouver en proximité, dans les
    magasins autour de vous, ce que vous cherchez. Et que progressivement, nous puissions vous faire
    découvrir des magasins que vous ne connaissiez pas et dans lesquels vous ne seriez pas aller
    naturellement.
</p>

<h3>Est-ce que je peux trouver tous les commerçants autour de moi ? Comment sont référencés les
commerçants dans l’application ?</h3>

<p>
    Les commerçants, pour être référencés, doivent télécharger l’application Ouilocaly sur l’App Store ou
    le Play Store et se créer un compte commerçant. L’Espace Commerçant est accessible directement
    depuis le menu de l’application.
    Plus il y a de commerçants et plus les résultats que vous obtiendrez seront complets et précis. Pour
    cela, nous allons tous les jours à la rencontre des commerçants pour leur présenter Ouilocaly et les
    encourager à rejoindre l’application.
    En tant qu’utilisateur, vous avez aussi un rôle à jouer ! Directement dans l’application, en nous
    proposant vos magasins préférés qui ne seraient pas encore sur l’application afin que nous puissions
    les contacter pour qu’ils nous rejoignent, mais aussi en nous indiquant les changements concernant
    tel ou tel magasin. Vous pouvez également directement leur parler de nous.
</p>

<h3>Comment gagnez-vous de l’argent ? Quel est votre modèle économique ?</h3>

<p>
    Le modèle économique repose sur un abonnement annuel de 118,80€ pour les commerçants s’ils
    souhaitent créer des ventes produits (ventes avec photo, produit et prix) et accéder à leurs
    statistiques d’activité (combien de personnes ont vu ma page, mes ventes, combien de personnes
    ont eu l’intention de venir en magasin et combien de personnes sont intéressées par les produits ou
    les marques que je propose).
    L’inscription, elle, est gratuite, ainsi que la création et le référencement de la page magasin ainsi que la possibilité pour les commerçants de créer des offres génériques.
</p>

<h3>Pourquoi ce modèle économique ?</h3>

<p>
    Notre souhait est de vous apporter une exhaustivité dans les résultats et de la diversité en vous
    faisant découvrir un maximum de magasins, basé sur ce qui vous intéresse ou ce que vous cherchez.
    Pour cela, il est nécessaire de s’adresser au plus grand nombre, ce que permet ce système
    d’abonnement. Ainsi, tous les commerçants, qu’ils soient petits ou gros, anciens ou récents, peuvent
    être référencés et utiliser Ouilocaly.
</p>

<h3>Qu’en est-il de nos données personnelles ?</h3>

<p>
    Ouilocaly n’exploite ni ne revend vos données personnelles. En atteste le choix de ne pas faire de
    publicité sur l’application ainsi que la possibilité d’accéder et d’utiliser l’application sans avoir à créer de compte. Le modèle de Ouilocaly repose sur vos configurations Favoris et Notifications. Celles-ci
    nous permettent de personnaliser l’application suivant ce que vous souhaitez et de vous alerter. Ces
    configurations sont accessibles et modifiables en permanence, vous avez la main dessus. Pas de
    configurations, pas d’alerte et une application générique sans mise en avant des Favoris, c’est aussi
    simple que cela.
</p>

<h3>A quoi servent les alertes ?</h3>

<p>
    Les alertes servent bien entendu à vous alerter sur l’offre qui vous intéresse, basé sur ce que vous
    avez indiqué : marques, produits, à partir de quel prix, et boutiques.
    Toutefois, notre souhait est d’aller plus loin et de créer une véritable place d’échanges avec vos
    commerçants. Comment ?<br />
    Vos configurations marques et produits sont centralisés anonymement de notre côté afin que nous
    puissions les réinterpréter et les transmettre aux commerçants de l’autre côté, sur leur interface
    dans la partie statistique, pour leur donner des clés de lecture et les aider à créer leurs ventes pour vous.<br/>
    Concrètement, si 1000 personnes suivent la marque Moulin Roty en Favoris, c’est une information
    que nous donnons aux commerçants sur leur interface. En tapant, Moulin Roty, ils sauront que 1000
    personnes sont intéressées. Et cela pour les 21000 marques référencées sur Ouilocaly.<br/>
    Il en va de même avec les produits. Si 1000 personnes sont intéressées par des aspirateurs. On
    l’indique aux commerçants.<br />
    Nous souhaitons ainsi créer une place d’échanges entre vous et vos commerçants. Que vous puissiez
    indiquer ce que vous souhaitez et être alertés, et que vos commerçants puissent comprendre
    simplement ce qu’il se passe autour d’eux, ce que souhaitent leurs clients et ainsi les aider dans la
    création de leurs ventes.
</p>

<h3>Et la géolocalisation ?</h3>

<p>
    La géolocalisation activée nous permet de connaitre votre position afin de vous indiquer les
    commerces autour de vous le temps de votre session. Il s’agit du système le plus précis. Elle est
    activable et désactivable directement depuis votre téléphone. Si vous ne souhaitez pas l’activer, vous
    pouvez tout de même connaitre les commerces autour de vous en indiquant une adresse. Cette
    adresse sera active le temps de la session et désactivée au moment où vous quitterez l’application.
    Nous avons voulu vous laisser le choix en vous proposant des alternatives.
</p>

<h3>Est-il possible d’acheter directement sur Ouilocaly les produits en magasin ?</h3>

<p>
    Il n’est aujourd’hui pas possible d’acheter des produits en magasin depuis Ouilocaly. Notre objectif
    est de vous informer et de créer du lien avec les commerçants autour de vous afin que vous puissiez
    vous rendre en boutique.
</p>


<h3>A quoi correspondent les ventes privées et les produits affichés ?</h3>

<p>
    Les ventes privées correspondent aux ventes des principaux sites de ventes privées en France. Les
    produits affichés correspondent aux produits en promotion de e-marchands ayant des points de
    vente physiques.
</p>

