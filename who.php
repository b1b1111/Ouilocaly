<div id="text_why">
    <h3>Pourquoi Ouilocaly ?</h3>

    <p>
        Nous étions ados dans les années 90, juste avant l’essor d’Internet. Nous avons à la fois connu la vie sans Internet où E-commerce, réseaux sociaux et autres moteurs de recherche n’existaient pas et à la fois grandi avec Internet. Faire la tournée des magasins de jeux vidéo le samedi après-midi, des magasins de sport pour voir et revoir les chaussures que l’on ne pouvait pas s’acheter, lire des bandes dessinées assis en rayon, ou écouter des CDs pendant des heures aux points d’écoute, autant d’activités qui ont animé notre jeunesse. En même temps que nous découvrions émerveillés Internet, une ouverture vers l’extérieur, les premiers envois d’email, les forums, etc. malgré les connexions incertaines, temporaires et les coupures téléphoniques.
    </p>

    <p>
        Aujourd’hui, comme la majorité d’entre vous, nous achetons aussi bien en magasin que sur Internet.
        Cela dit, acheter en magasin nous tient particulièrement à cœur. Nous nous sommes à ce titre rendus
        compte que la proximité avait changé de camp. La proximité est maintenant dans notre poche sur
        notre smartphone et paradoxalement moins dans les commerces dit « de proximité ».
    </p>

    <p>
        Si nous cherchons un produit ou une marque, il est très simple d’avoir de nombreuses réponses sur
        Internet, disponibilité des produits, prix, caractéristiques, etc., que ce soit des produits neufs chez les e-marchands ou d’occasion de particulier à particulier. Par contre, ce l’est beaucoup moins dans les
        commerces qui nous entourent.
    </p>

    <p>
        Pour une simple raison, cette information n’existe pas ou est très difficilement accessible.
    </p>

    <p>
        Nous nous sommes tous posés la question de savoir où acheter le produit que nous recherchons,
        dans quel magasin le trouver. Bien entendu, nous savons tous que nous aurons plus de chance de
        trouver un livre dans une librairie, un pantalon dans un magasin de vêtement, une poussette dans un
        magasin de puériculture ou une perceuse dans un magasin de bricolage mais quel produit ? A quel
        prix ? Est-il disponible ? d’où vient-il ? comment a-t-il été fabriqué ? etc.
    </p>

    <p>
        Résultat, nous finissons par acheter le produit sur Internet, ne sachant pas où le trouver dans les magasins autour de nous.
    </p>

    <p>
        Cela est d’autant plus paradoxal que, comme la majorité des Français (65% selon une étude
        Bazaarvoice), nous préférons aller en magasin afin de voir les produits, les toucher et repartir
        immédiatement avec.
    </p>

    <p>
        Nous nous sommes donc rendus compte qu’il fallait remédier à ce problème d’inversion de la
        proximité en permettant à chacun d’entre nous de savoir où acheter localement le produit ou la
        marque qu’il recherche.
    </p>

    <p>
        C’est donc pour cela que nous avons créé Ouilocaly avec une mission simple : rapprocher les
        commerces de proximité d’utilisateurs de plus en plus connectés.
    </p>

    <h3>POUR UN COMMERCE VIVANT, DIVERS, LOCAL, SOURCE DE LIEN</h3>

    <p>
        Au travers de Ouilocaly, nous souhaitons mettre en avant une certaine forme de commerce, vivant,
        divers, créatif, local et source de lien social.
    </p>
    <p>
        Le commerce de proximité et les magasins physiques sont partie intégrante de nos villes et de nos
        centres-villes, acteurs non seulement de leur dynamisme et de leur animation mais aussi de leur
        identité. Ce sont des marqueurs importants des territoires desquels peuvent se dégager un savoir-
        faire, voir une tradition, des métiers.
    </p>
    <p>
        Derrière chaque produit, suivant où on l’achète, on peut s’imaginer une histoire, ce n’est pas juste un produit que l’on consomme et que l’on jette. Cette histoire, comme client, on peut la partager avec les commerçants. Ces derniers, plus que des influenceurs, sont de véritables spécialistes, à même de nous conseiller, de nous partager leur savoir et leur passion.
    </p>
    <p>
        Ce sont eux également, grâce à leur expertise, qui vont être les plus à mêmes de proposer les
        meilleurs produits, des nouveautés, parfois même des produits exclusifs, de créateurs ou de
        collections que l’on ne trouve pas sur Internet, que l’on va pouvoir toucher, essayer. Le commerce de proximité, c’est de la découverte, mélange de créativité et de diversité.
    </p>
    <p>
        Acheter un produit pour soi ou pour l’offrir en cadeau procure très souvent une satisfaction. Le
        trouver et l’acheter en magasin relève du plaisir. Que cela soit chez un commerçant indépendant va
        même plus loin et nous procure de la fierté, entre partage et complicité avec le commerçant, et le
        sentiment de participer à la vie et au développement de là où nous sommes, là où nous habitons, là
        où nous vivons.
    </p>
    <p>
        Nous espérons qu’au travers de Ouilocaly, c’est un peu de cette vision que nous pourrons vous
        partager.
    </p>

